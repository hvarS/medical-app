    def get_trimask(self):
        img_name = self.openImage.split('/')[-1][:-4]
        mask_root = os.path.join(os.getcwd(),'temp_saver','masks',img_name)
        trimask_save_path = os.path.join(os.getcwd(),'temp_saver','rgb_trimask',img_name)

        if not os.path.exists(trimask_save_path):
            os.makedirs(trimask_save_path)

        crypts_mask = os.path.join(mask_root,'Crypts')
        villi_mask = os.path.join(mask_root,'Villi')
        epithelium_mask = os.path.join(mask_root,'Epithelium')
        brunner_mask = os.path.join(mask_root,'Brunner\'s Gland')

        filename = img_name+'.png'
        w,h = self.canvas.pixmap.width(), self.canvas.pixmap.height()
        rgb_mask = np.zeros((h, w, 3))
        rgb_mask[:, :, 0] += np.array(cv2.imread(os.path.join(crypts_mask, filename),cv2.IMREAD_GRAYSCALE)) / 255
        rgb_mask[:, :, 1] += np.array(cv2.imread(os.path.join(villi_mask, filename),cv2.IMREAD_GRAYSCALE)) / 255
        rgb_mask[:, :, 2] += np.array(cv2.imread(os.path.join(epithelium_mask, filename),cv2.IMREAD_GRAYSCALE)) / 255
        rgb_mask[:, :, :] += np.expand_dims(np.array(cv2.imread(os.path.join(brunner_mask, filename),cv2.IMREAD_GRAYSCALE)), -1) * 0.5 / 255.
        rgb_mask = np.clip(rgb_mask, 0., 1.)
        rgb_mask = np.uint8(rgb_mask * 255)

        img_save_loc = os.path.join(trimask_save_path,img_name+'.png')
        cv2.imwrite(img_save_loc,rgb_mask)

        return trimask_save_path

