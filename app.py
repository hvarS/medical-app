    def actualMeasurement(self):
        clssdict = {}
        clsscnt = {}

        try:
            self.backupDict
        except:
            self.backupDict = {}
        
        if self.backupDict != {}:
            self.backupDict = self.statsDict

        for shape in self.canvas.shapes:
            lbl = shape.label
            if  lbl not in clsscnt:
                clsscnt[lbl] = 1
            else:
                clsscnt[lbl] += 1
            points = shape.points

            ## Removing Duplicate Points in the Annotations
            new_points_list = []
            new_points_list.insert(0,points[0])
            for i,_ in enumerate(points):
                if i != 0:
                    if points[i]==points[i-1]:
                        continue
                    else:
                        new_points_list.append(points[i])
            shape.points = new_points_list
            ###############################################

            dist = 0
            prev_point = int(points[0].x()),int(points[0].y())

            for idx in range(1,len(points)):
                cur_point = int(points[idx].x()),int(points[idx].y())
                dist += math.sqrt((cur_point[0]-prev_point[0])**2+(cur_point[1]-prev_point[1])**2)
                prev_point = cur_point

            if self.CALIBRATION_INDEX!=-1:
                CALIBRATION_CONSTANT = CALIBRATE[self.CALIBRATION_INDEX]
            else:
                CALIBRATION_CONSTANT = self.CUSTOM_CALIBRATION


            shape.other_data['Measured: '+str(lbl)+'['+str(clsscnt[lbl])+']'] = str(round(dist * CALIBRATION_CONSTANT,4)) + ' µm'
            shape.other_data['Pixel Length'] = str(round(dist))

            clssdict['Measured Label -> '+str(lbl)+'['+str(clsscnt[lbl])+']'] = '\t'+ \
                                                                        str(round(dist * CALIBRATION_CONSTANT,4)) +\
                                                                        ' µm'


        self.statsDict = clssdict
        self.addStats()

