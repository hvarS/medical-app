    def get_corrected_interpretable_image_mask(self):
        temp_save_dir = os.path.join(os.getcwd(),'temp_saver','results','counting_images')
        if not os.path.exists(temp_save_dir):
            os.makedirs(temp_save_dir)

        img = cv2.imread(self.openImage)
        img_name = self.openImage.split('/')[-1][:-4] 
        
        w, h = self.canvas.pixmap.width(), self.canvas.pixmap.height()
        mask_array = np.zeros((w,h),dtype=np.int32)
        
        for shape in self.canvas.shapes:
            points = shape.points
            contour = []
            for point in points:
                contour.append([int(point.x()),int(point.y())])
            cv2.fillPoly(mask_array, pts =[np.array(contour)], color=(255,255,255))

        mask_array[mask_array == 255] = 1
        selected_mask = np.expand_dims(mask_array, axis=-1)



        interpretable_img = img*selected_mask
        img_write_path = os.path.join(temp_save_dir,img_name+'.png')

